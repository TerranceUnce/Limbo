function is_mobile(){
	return ($('.navbar .btn-navbar').is(':visible'));
}

function check_hover_nav(){
	$('.navbar').find('.up-arrow').remove();
	if(is_mobile()){
		$('.navbar').removeClass('hover-nav');
	
		$('.navbar ul.menu-level-0 li.has-children').hover(			
			function(){ return false; },
			function(){ return false; }
		);
	}
	else{
		$('.navbar').addClass('hover-nav');
		$('.navbar ul.menu-level-0 li.has-children ul.menu-level-1').prepend('<li class="up-arrow"></li>');
		$('.navbar ul.menu-level-0 li.has-children').hover(			
			function(){ $( this ).find('ul.menu-level-1').fadeIn(200); $(this).addClass('show');  },
			function(){ $( this ).find('ul.menu-level-1').fadeOut(40); $(this).removeClass('show'); }
		);
		
	}
	
}

$(document).ready(function(){
	check_hover_nav();

	//Dropdown Navigation//	
	$(window).resize(check_hover_nav);
		
});




